    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        $user->syncPermissions();
        $user->syncRoles();

        User::destroy($id);
        

        return back()->withSuccess(trans('app.success_destroy'));
    }
}
